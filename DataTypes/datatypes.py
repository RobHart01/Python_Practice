# Getting data type
# You can get the data type of any object by using the type() function:

a = 5
print(type(a))
b = "Robert"
print(type(b))
c = 3.14
print(type(c))
d = [1,2,3]
print(type(d))
e = ("Crawl", "Walk", "Run")
print(type(e))
f = range(10)
print(type(f))
g = {"name" : "Robert", "age" : 22}
print(type(g))
h = {"Coffee", "Code", "Repeat"}
print(type(h))
i = frozenset({"Coffee", "Code", "Repeat"})
print(type(i))
j = True
print(type(j))
k = b"Hello"
print(type(k))
